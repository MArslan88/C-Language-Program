#include <stdio.h>

int main() {

    int a=1;
    // increment
    // 1- Post increment (a++)
    // 2- Pre increment (++a)

    // a = a+1; // a++
    // a++;
    printf("%d\n",a++);
    printf("%d\n",++a);

    // decrement
    // 1- Post decrement (a--)
    // 2- Pre decrement (--a)

    int b = 5;

    printf("%d\n",b--);
    printf("%d\n",b);



        return 0;
}